    "def position_size(ev, vol_daily, horizon=3, max_risk=0.02):\n",
    "    if vol_daily <= 0 or ev <= 0:\n",
    "        return 0.0\n",
    "    vol_horizon = vol_daily * np.sqrt(horizon)\n",
    "    if vol_horizon == 0:\n",
    "        return 0.0\n",
    "    kelly = ev / (vol_horizon ** 2)\n",
    "    return min(max(kelly * 0.5, 0), max_risk)\n",
