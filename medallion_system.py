    "def risk_metrics(multipliers, current_price):\n",
    "    rets = multipliers - 1\n",
    "    ev = rets.mean()\n",
    "    downside_prob = (rets < 0).mean()\n",
    "    tail = np.sort(rets)[:max(1, int(0.05 * len(rets)))]\n",
    "    es = tail.mean() if len(tail) > 0 else 0\n",
    "    return ev, downside_prob, es\n",
    "\n",
    "\n",
    "def expectation_agent(ev, downside_prob, es):\n",
    "    if ev > 0 and downside_prob < 0.45 and es > -0.03:\n",
    "        return \"BUY\"\n",
    "    elif ev < -0.005:\n",
    "        return \"SELL\"\n",
    "    return \"HOLD\"\n",
    "\n",
    "\n",
    "def structure_agent(df):\n",
    "    if len(df) == 0:\n",
    "        return \"BLOCK\"\n",
    "    last = df.iloc[-1]\n",
    "    if (last.get('trend_signal', 0) == 1 and \n",
    "        last.get('vol_ratio', 999) < 1.5 and \n",
    "        last.get('regime_prob', 0) > 0.6):\n",
    "        return \"ALLOW\"\n",
    "    return \"BLOCK\"\n",
    "\n",
    "\n",
    "def ml_agent(df, model):\n",
    "    if model is None:\n",
    "        return \"HOLD\"\n",
    "    features = ['trend_signal', 'vol_ratio', 'bb_width', 'volume_z', 'skew_20', 'kurt_20']\n",
    "    features = [f for f in features if f in df.columns]\n",
    "    if not features:\n",
    "        return \"HOLD\"\n",
    "    last_row = df[features].tail(1)\n",
    "    if last_row.isna().any().any():\n",
    "        return \"HOLD\"\n",
    "    prob_buy = model.predict_proba(last_row)[0, 1]\n",
    "    if prob_buy > 0.62:\n",
    "        return \"BUY\"\n",
    "    elif prob_buy < 0.38:\n",
    "        return \"SELL\"\n",
    "    return \"HOLD\"\n",
    "\n",
    "\n",
    "def final_decision(A, B, C):\n",
    "    votes = [A, B, C]\n",
    "    if \"SELL\" in votes:\n",
    "        return \"SELL\"\n",
    "    if votes.count(\"BUY\") >= 2 and B == \"ALLOW\":\n",
    "        return \"BUY\"\n",
    "    return \"HOLD\"\n",
    "\n",
