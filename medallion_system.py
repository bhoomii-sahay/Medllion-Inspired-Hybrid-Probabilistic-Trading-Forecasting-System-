    "\n",
    "def fetch_data(ticker, years=10):\n",
    "    end = datetime.today() - timedelta(days=1)\n",
    "    start = end - timedelta(days=365 * years)\n",
    "    \n",
    "    df = yf.download(ticker, start=start, end=end, auto_adjust=True, progress=False)\n",
    "    \n",
    "    if isinstance(df.columns, pd.MultiIndex):\n",
    "        df.columns = df.columns.get_level_values(0).str.lower()\n",
    "    else:\n",
    "        df.columns = df.columns.str.lower()\n",
    "    \n",
    "    expected = ['open', 'high', 'low', 'close', 'volume']\n",
    "    missing = [col for col in expected if col not in df.columns]\n",
    "    if missing:\n",
    "        raise ValueError(f\"Missing columns for {ticker}: {missing}\")\n",
    "    \n",
    "    return df[expected].dropna()\n",
    "\n",
    "\n",
    "def compute_features(df, index_df=None):\n",
    "    df = df.copy()\n",
    "    \n",
    "    df['return']     = df['close'].pct_change()\n",
    "    df['log_return'] = np.log1p(df['return'])\n",
    "    \n",
    "    df['sma_5']      = df['close'].rolling(5).mean()\n",
    "    df['sma_20']     = df['close'].rolling(20).mean()\n",
    "    df['trend_signal'] = (df['sma_5'] > df['sma_20']).astype(int)\n",
    "    \n",
    "    df['vol_10']     = df['return'].rolling(10).std()\n",
    "    df['vol_ratio']  = df['vol_10'] / df['vol_10'].rolling(60).mean()\n",
    "    \n",
    "    std20 = df['close'].rolling(20).std()\n",
    "    df['bb_mid']     = df['close'].rolling(20).mean()\n",
    "    df['bb_width']   = (2 * std20) / df['bb_mid']\n",
    "    \n",
    "    df['volume_z']   = (df['volume'] - df['volume'].rolling(50).mean()) / df['volume'].rolling(50).std()\n",
    "    \n",
    "    df['skew_20']    = df['return'].rolling(20).apply(skew, raw=True)\n",
    "    df['kurt_20']    = df['return'].rolling(20).apply(kurtosis, raw=True)\n",
    "    \n",
    "    if index_df is not None and not index_df.empty:\n",
    "        index_df = index_df.copy()\n",
    "        index_df['return'] = index_df['close'].pct_change()\n",
    "        index_df = index_df.reindex(df.index).ffill()\n",
    "        if 'return' in index_df.columns and index_df['return'].notna().any():\n",
    "            df['rel_strength'] = df['return'] - index_df['return']\n",
    "    \n",
    "    return df.dropna()\n",
    "\n",
    "\n",
    "def stationary_block_bootstrap(returns, horizon=3, sims=5000, block_size=15):\n",
    "    if len(returns) < horizon:\n",
    "        return np.ones(sims)\n",
    "    T = len(returns)\n",
    "    multipliers = np.zeros(sims)\n",
    "    \n",
    "    for i in range(sims):\n",
    "        sim_ret = []\n",
    "        while len(sim_ret) < horizon:\n",
    "            start = np.random.randint(0, T)\n",
    "            length = np.random.geometric(p=1.0 / block_size)\n",
    "            block = returns[start:start + length]\n",
    "            sim_ret.extend(block)\n",
    "        sim_ret = np.array(sim_ret[:horizon])\n",
    "        multipliers[i] = np.exp(np.sum(sim_ret))\n",
    "    \n",
    "    return multipliers\n",
    "\n",
    "\n",
    "def classify_regime(df):\n",
    "    cols = ['vol_10', 'bb_width', 'skew_20', 'kurt_20']\n",
    "    features = df[cols].dropna()\n",
    "    if len(features) < 20:\n",
    "        df['regime'] = 0\n",
    "        df['regime_prob'] = 0.50\n",
    "        return df\n",
    "    \n",
    "    gmm = GaussianMixture(n_components=3, random_state=42, covariance_type='full')\n",
    "    regimes = gmm.fit_predict(features)\n",
    "    probs = gmm.predict_proba(features)\n",
    "    \n",
    "    df = df.loc[features.index].copy()\n",
